  @Override
  public void enumScalarChange(EnumScalarEvent ese) {
    short value = event.getShortValueFromEnumScalar(ese.getValue());
    if( value==0 )
      eventPanel.setBackground(ATKConstant.getColor4State("STANDBY"));
    else if (value==1 )
      eventPanel.setBackground(ATKConstant.getColor4State("ON"));
    else
      eventPanel.setBackground(ATKConstant.getColor4State("UNKNOWN"));
  }

  @Override
  public void stateChange(AttributeStateEvent ase) {
  }

  @Override
  public void errorChange(ErrorEvent ee) {
    eventPanel.setBackground(ATKConstant.getColor4State("UNKNOWN"));
  }
  
