  public ADCSetupPanel(String devName) {
    
    initComponents();
    
    this.devName = devName;
    attList = new AttributePolledList();
    attList.addErrorListener(errWin);

    try {
      
      NumberScalar delay = (NumberScalar)attList.add(devName+"/ADC_FILTER_DELAY_S");
      delayEditor.setModel(delay);
      NumberScalar overflow = (NumberScalar)attList.add(devName+"/ADC_OVF_LIMIT_S");
      overflowEditor.setModel(overflow);
      NumberScalar minmax = (NumberScalar)attList.add(devName+"/ADC_EVENT_LIMIT_S");
      minmaxEditor.setModel(minmax);      
      EnumScalar loopback = (EnumScalar)attList.add(devName+"/ADC_LOOPBACK_S");
      loopbackComboEditor.setEnumModel(loopback);
      EnumScalar mmsInput = (EnumScalar)attList.add(devName+"/ADC_MMS_SOURCE_S");
      mmsInputComboEditor.setEnumModel(mmsInput);
      EnumScalar dramInput = (EnumScalar)attList.add(devName+"/ADC_DRAM_SOURCE_S");
      dramInputComboEditor.setEnumModel(dramInput);
      NumberSpectrum filter = (NumberSpectrum)attList.add(devName+"/ADC_FILTER_S");
      filterViewer.setModel(filter);
      EnumScalar status = (EnumScalar)attList.add(devName+"/ADC_MMS_OVERFLOW");
      statusViewer.setModel(status);
      statusViewer.setBackgroundColor(Color.WHITE);
      event = (EnumScalar)attList.add(devName+"/ADC_EVENT");
      event.addEnumScalarListener(this);
      
    } catch (ConnectionException ex) {      
    }
    
    filterViewer.setHeader("ADC compensation filter");
    filterViewer.getDataView().setMarker(JLDataView.MARKER_DOT);
    filterViewer.setPreferredSize(new Dimension(200,200));
    filterViewer.getXAxis().setGridVisible(true);
    filterViewer.getY1Axis().setGridVisible(true);
    
    attList.setRefreshInterval(1000);
    
    setTitle("ADC Setup [" + devName + "]");
    ATKGraphicsUtils.centerFrameOnScreen(this);
    
  }
  
  public void setVisible(boolean visible) {
    if(visible)
      attList.startRefresher();
    else
      attList.stopRefresher();    
    super.setVisible(visible);
  }

